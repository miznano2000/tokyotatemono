    calculateOverallRisk(quarterData) {
        const risks = [
            quarterData.politicalRisk?.level,
            quarterData.economicRisk?.level,
            quarterData.humanRightsRisk?.level
        ].filter(Boolean);

        if (risks.includes('high')) return 'high';
        if (risks.includes('medium')) return 'medium';
        return 'low';
    }

    getRiskLabel(level) {
        const labels = {
            high: '高',
            medium: '中',
            low: '低'
        };
        return labels[level] || '不明';
    }

    getPriorityLabel(priority) {
        const labels = {
            high: '高',
            medium: '中',
            low: '低'
        };
        return labels[priority] || '不明';
    }

    formatTime(timestamp) {
        return new Date(timestamp).toLocaleString('ja-JP', {
            month: '2-digit',
            day: '2-digit',
            hour: '2-digit',
            minute: '2-digit'
        });
    }

