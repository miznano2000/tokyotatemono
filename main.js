    renderEconomicChart(chartId, title, data) {
        // Chart.jsが利用可能な場合のみチャートを描画
        if (typeof Chart !== 'undefined') {
            setTimeout(() => {
                const canvas = document.getElementById(chartId);
                if (canvas) {
                    const ctx = canvas.getContext('2d');

                    // 既存のチャートがあれば破棄
                    if (canvas.chart) {
                        canvas.chart.destroy();
                    }

                    // サンプルデータ（実際のデータに置き換え可能）
                    const chartData = {
                        labels: ['Q1 2024', 'Q2 2024', 'Q3 2024', 'Q4 2024', 'Q1 2025'],
                        datasets: [{
                            label: title,
                            data: data.values || [65, 70, 68, 72, 75],
                            borderColor: 'rgb(75, 192, 192)',
                            backgroundColor: 'rgba(75, 192, 192, 0.2)',
                            tension: 0.1
                        }]
                    };

                    canvas.chart = new Chart(ctx, {
                        type: 'line',
                        data: chartData,
                        options: {
                            responsive: true,
                            maintainAspectRatio: false,
                            scales: {
                                y: {
                                    beginAtZero: true,
                                    max: 100
                                }
                            },
                            plugins: {
                                legend: {
                                    display: false
                                }
                            }
                        }
                    });
                }
            }, 100);
        } else {
            // Chart.jsが利用できない場合は簡単なテキスト表示
            setTimeout(() => {
                const canvas = document.getElementById(chartId);
                if (canvas) {
                    const ctx = canvas.getContext('2d');
                    ctx.fillStyle = '#666';
                    ctx.font = '16px Arial';
                    ctx.textAlign = 'center';
                    ctx.fillText('チャートデータ: ' + (data.current || 'N/A'), canvas.width / 2, canvas.height / 2);
                }
            }, 100);
        }
    }

